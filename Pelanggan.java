public class Pelanggan {
    private String nama; 
    private String kontak;

    public Pelanggan(String nama, String kontak) {
        this.nama = nama;
        this.kontak = kontak;
    }

    // Getter
    public String getNama() {
        return nama;
    }

    public String getKontak() {
        return kontak;
    }
}
